public class TTphong {
    private int MATT;
    private String TT;
    private String CHUTHICH;

    public TTphong(int MATT, String TT, String CHUTHICH) {
        this.MATT = MATT;
        this.TT = TT;
        this.CHUTHICH = CHUTHICH;
    }

    public int getMATT() {
        return MATT;
    }

    public void setMATT(int MATT) {
        this.MATT = MATT;
    }

    public String getTT() {
        return TT;
    }

    public void setTT(String TT) {
        this.TT = TT;
    }

    public String getCHUTHICH() {
        return CHUTHICH;
    }

    public void setCHUTHICH(String CHUTHICH) {
        this.CHUTHICH = CHUTHICH;
    }
    
    
    
}
