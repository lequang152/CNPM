
package CNPM;


public class Phong {
    private String TENPHONG;
    private int MALP;
    private int MATTP;
    private int MASG;

    public Phong(String TENPHONG, int MALP, int MATTP, int MASG) {
        this.TENPHONG = TENPHONG;
        this.MALP = MALP;
        this.MATTP = MATTP;
        this.MASG = MASG;
    }

    public String getTENPHONG() {
        return TENPHONG;
    }

    public void setTENPHONG(String TENPHONG) {
        this.TENPHONG = TENPHONG;
    }

    public int getMALP() {
        return MALP;
    }

    public void setMALP(int MALP) {
        this.MALP = MALP;
    }

    public int getMATTP() {
        return MATTP;
    }

    public void setMATTP(int MATTP) {
        this.MATTP = MATTP;
    }

    public int getMASG() {
        return MASG;
    }

    public void setMASG(int MASG) {
        this.MASG = MASG;
    }

  
}
    
    
    
    
