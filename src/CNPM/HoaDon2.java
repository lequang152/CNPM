package CNPM;


public class HoaDon2 {
    private int MADP;
    private String TENPHONG;
    private int GIAPHONG;
    private int SONGAY;
    private int GIADV;;

    public HoaDon2(int MADP, String TENPHONG, int GIAPHONG, int SONGAY, int GIADV) {
        this.MADP = MADP;
        this.TENPHONG = TENPHONG;
        this.GIAPHONG = GIAPHONG;
        this.SONGAY = SONGAY;
        this.GIADV = GIADV;
    }

    public int getMADP() {
        return MADP;
    }

    public void setMADP(int MADP) {
        this.MADP = MADP;
    }

    public String getTENPHONG() {
        return TENPHONG;
    }

    public void setTENPHONG(String TENPHONG) {
        this.TENPHONG = TENPHONG;
    }

    public int getGIAPHONG() {
        return GIAPHONG;
    }

    public void setGIAPHONG(int GIAPHONG) {
        this.GIAPHONG = GIAPHONG;
    }

    public int getSONGAY() {
        return SONGAY;
    }

    public void setSONGAY(int SONGAY) {
        this.SONGAY = SONGAY;
    }

    public int getGIADV() {
        return GIADV;
    }

    public void setGIADV(int GIADV) {
        this.GIADV = GIADV;
    }

    
}
