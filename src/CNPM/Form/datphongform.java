        // TODO add your handling code here:
                // TODO add your handling code here
        if(jTextFieldTenKH.getText().equals("") || jTextFieldCMND.getText().equals("") || jTextFieldSDT.getText().equals("") ||jTextFieldTuoi.getText().equals("")
                || jTextFieldSoPhong.getText().equals("")|| jComboBoxPTTT.getSelectedItem().equals("Chọn") || jDateChooserNgayDatPhong.toString().equals("") 
                || jDateChooserCheckin.toString().equals("") || jDateChooserCheckout.toString().equals("")||tinhtrang.getSelectedIndex() == 0) {
                showMessageDialog(this, "Không có đủ thông tin");
                return;
        }
        int maphong = 0;
        Connection con = getConnection();
        try {
            Statement st = (Statement) con.createStatement();
            String query0 = "SELECT MAPHONG FROM khachsan.phong where TENPHONG = " + jTextFieldSoPhong.getText();
            ResultSet rs = st.executeQuery(query0);;
            while (rs.next()) {
                maphong = rs.getInt("MAPHONG");
            }
            
            // Tạo một đối tượng để thực hiện công việc
            
            String query1 = "INSERT INTO khachhang(MAKH, TENKH, CMND, SDT, QUOCTICH, GIOITINH, TUOI) VALUES('" + maxmkh + "',"
            + "'" + jTextFieldTenKH.getText() + "','" + jTextFieldCMND.getText() + "', '" + jTextFieldSDT.getText() + "', '" 
                    + jTextFieldQuocTich.getText() + "', '" + jTextFieldGioiTinh.getText() + "', '" + jTextFieldTuoi.getText()+ "')";
            
            String query2 = "INSERT INTO datphong(MADP, NGAYDAT, NGAYNHAN, NGAYTRA, TINHTRANG, PTTT, MAKH, MAPHONG) VALUES('" + maxmdp + "',"
            + "'" + new java.sql.Date(jDateChooserNgayDatPhong.getDate().getTime()) + "','" + new java.sql.Date(jDateChooserCheckin.getDate().getTime()) 
                    + "', '" + new java.sql.Date(jDateChooserCheckout.getDate().getTime()) + "', '" + tinhtrang.getSelectedItem().toString() + "', '"
                    + jComboBoxPTTT.getSelectedItem() + "', '" + maxmkh + "', '" + maphong+ "')";


            st.execute(query1);
            st.execute(query2);

            showMessageDialog(this, "Đặt phòng thành công");
            
            String query3 = "UPDATE phong set MATTP = "+ "'2'" +" where MAPHONG = " + "'" + maphong + "'" ;
            st.execute(query3);

        } catch (Exception ex) {
            ex.printStackTrace();
            showMessageDialog(this, "thông tin không hợp lệ");
        }
