    public void search() {
        TableRowSorter<TableModel> rowSorter = new TableRowSorter<>(DSPhongTrong.getModel());
        DSPhongTrong.setRowSorter(rowSorter);
        if (loaiphong.getSelectedIndex() == 0 && sogiuong.getSelectedIndex() == 0) {
            rowSorter.setRowFilter(null);
        }  else if (loaiphong.getSelectedIndex() == 0) {
            rowSorter.setRowFilter(RowFilter.regexFilter("(?i)" + sogiuong.getSelectedItem().toString()));
        }else if (sogiuong.getSelectedIndex() == 0) {
            rowSorter.setRowFilter(RowFilter.regexFilter("(?i)" + loaiphong.getSelectedItem().toString()));
        }else
            {
                List<RowFilter<Object,Object>> filters = new ArrayList<RowFilter<Object,Object>>(2);
                filters.add(RowFilter.regexFilter(loaiphong.getSelectedItem().toString(), 1));
                filters.add(RowFilter.regexFilter(sogiuong.getSelectedItem().toString(), 2));
                rowSorter.setRowFilter(RowFilter.andFilter(filters));
            }
  }
    
