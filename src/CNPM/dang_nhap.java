    public ArrayList<User> layDanhSachDanhnhap() {
        ArrayList<User> dsus = new ArrayList<User>();
        Connection con = getConnection();
        try {
            Statement st = (Statement) con.createStatement();
            String sql = "SELECT USER, PASSWORD, Loai FROM khachsan.dangnhap;";

            ResultSet rs = st.executeQuery(sql);

            User us;
            while (rs.next()) {
                us = new User(rs.getString("USER"), rs.getString("PASSWORD"), rs.getString("Loai"));

                //Thêm vào danh sách
                dsus.add(us);
            }
            
        } catch (Exception ex) {
            showMessageDialog(this, "lỗi database");

        }
        
    
        return dsus;
    }

