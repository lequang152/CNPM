    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(User.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Vui lòng đăng nhập tài khoản");
        } else if(Password.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Vui lòng đăng nhập mật khẩu cũ");
        } else{
            for (User user : layDanhSachDanhnhap())
            {
            if(User.getText().equals(user.getUsername())){
            if(DigestUtils.sha256Hex(Password.getText()).equals(user.getPassword())){
                loggedInUser = user;
                break;

            }
            }
            }
        if (loggedInUser != null)
        {
            if(loggedInUser.getType().equals("1")) {
                JOptionPane.showMessageDialog(this, "đăng nhập thành công với tài khoản " + loggedInUser.getUsername());
                MENUFORM ql=new MENUFORM();
                ql.setVisible(true);
                this.setVisible(false);
            }
            if(loggedInUser.getType().equals("0")) {
                JOptionPane.showMessageDialog(this, "đăng nhập thành công với tài khoản " + loggedInUser.getUsername());
                MENUKHFORM ql=new MENUKHFORM();
                ql.pack();
                ql.setLocationRelativeTo(null);
                ql.setVisible(true);
                this.setVisible(false);
            }
        }
        else
        {
            JOptionPane.showMessageDialog(this, "sai tài khoản/mật khẩu");
        }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

